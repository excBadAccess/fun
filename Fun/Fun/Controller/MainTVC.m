#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    [tableView deselectRowAtIndexPath:indexPath animated:YES];//取消选中状态
    
    if (indexPath.row > 0) {
        
        [self.revealSideViewController popViewControllerAnimated:YES];
        
//        NSString * title = [NSString stringWithString:[self.arrDataSource objectAtIndex:indexPath.row]];
             
        NSNumber * titleNum = [NSNumber numberWithInt:indexPath.row];
        
        NSDictionary * dic = [NSDictionary dictionaryWithObjectsAndKeys:titleNum,@"key", nil];
        
        [[NSNotificationCenter defaultCenter]postNotificationName:@"message" object:nil userInfo:dic];//向消息中心传值
    }
}

